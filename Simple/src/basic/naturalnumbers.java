package basic;

public class naturalnumbers {

	public static void main(String[] args) {
		
	    int i, num = 10, sum = 0;  
	  
	    for(i = 0; i <= num; ++i)  
	    {  
	     
	    sum = sum + i;  
	    }  
	   
	    System.out.println("Sum of First 10 Natural Numbers is = " + sum);  
	    }  
	     

	}


