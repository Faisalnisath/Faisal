package basic;

public class operators {

	public static void main(String[] args) {
		int x = 10;
		int y = 20;

		System.out.println(x+=10);
		System.out.println(y%=10);
		System.out.println(x>=10);
		System.out.println(y<=10);
		System.out.println(x==10);
		System.out.println(y==10 && x>=10);
		System.out.println(x==10 || x<=10);
		System.out.println(y!=10);
		


	}

}

