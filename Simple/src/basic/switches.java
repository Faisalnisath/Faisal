package basic;

public class switches {

	public static void main(String[] args) {
	
			int data= 4;
			
			switch(data) {
				
			case 1:
				System.out.println("data is secured");
				break;
				
			case 2:
				System.out.println("data is private");
				break;
				
			case 3:
				System.out.println("data is public");
				break;
				
			case 4:
				System.out.println("data is protected");
				break;
				
			default:
				System.out.println("data is insecured");
				
				
				
		}
			

	}

}
